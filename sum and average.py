#find the sum and average from a list of numbers

numbers = [99, 84, 76, 50, 12, 31, 81, 45, 23, 16]

#sort the number in ascending order
numbers.sort()
print(numbers)

#find the sum of numbers
total_sum = sum(numbers)
print("Sum of numbers: ", total_sum)

#find the average
total_sum = sum(numbers)
total_average = total_sum / len(numbers)
print("The total Average: ", total_average)
